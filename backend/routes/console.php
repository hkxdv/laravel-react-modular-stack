<?php

declare(strict_types=1);

use Illuminate\Foundation\Inspiring;
use Illuminate\Support\Facades\Artisan;

/*
|--------------------------------------------------------------------------
| Rutas de Consola (Artisan Commands)
|--------------------------------------------------------------------------
|
| Este archivo es donde puedes definir todos tus comandos de consola
| personalizados basados en clausuras. Cada clausura está vinculada a una
| instancia de comando, lo que permite un punto de entrada ligero para
| interactuar con los métodos de E/S de cada comando.
|
*/

/**
 * Comando de ejemplo de Laravel para mostrar una cita inspiradora.
 * php artisan inspire
 */
Artisan::command('inspire', function () {
    $this->comment(Inspiring::quote());
})->purpose('Display an inspiring quote');
